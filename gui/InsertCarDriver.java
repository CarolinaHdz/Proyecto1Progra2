    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        
         if (jtfName.getText().equals("") || jtfSurname.getText().equals("")
            || jtfID.getText().equals("") || jtfNumberOfEmployee.getText().equals("") 
            || jtfWorkedHours.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Error! There are blank spaces");//verifica espacios en blanco
        } else {
             try {
                 boolean qualified;
                 boolean turn;
                 if (comboQualify.getSelectedItem()=="Yes") {
                     qualified=true;
                 } else {
                     qualified=false;
                 }
                 if (comboTurn.getSelectedItem()=="Day") {
                     turn=true;
                 } else {
                     turn=false;
                 }
                 
                 CarDriveFile carFile = new CarDriveFile(Path.my_carDriver_list_path);
                 if (!carFile.getByIDBoolean(jtfID.getText())) {
                     try {
                         CarDriver carDriver= new CarDriver( jtfName.getText(), jtfSurname.getText(), jtfID.getText()
                                 , jtfNumberOfEmployee.getText(), qualified,(String) comboLicense.getSelectedItem(),
                                 Integer.parseInt(jtfWorkedHours.getText())
                                 , turn, 1);
                         
                         carFile.saveCarDriver(carDriver);
                         JOptionPane.showMessageDialog(null,"The employee "+jtfName.getText()+" has been inserted successfully");
                     } catch (IOException ex) {
                         Logger.getLogger(InsertCarDriver.class.getName()).log(Level.SEVERE, null, ex);
                     } catch (ClassNotFoundException ex) {
                         Logger.getLogger(InsertCarDriver.class.getName()).log(Level.SEVERE, null, ex);
                     }
                 } else {
                     JOptionPane.showMessageDialog(null, "This ID already exists");
                 }
             } catch (IOException ex) {
                 Logger.getLogger(InsertCarDriver.class.getName()).log(Level.SEVERE, null, ex);
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(InsertCarDriver.class.getName()).log(Level.SEVERE, null, ex);
             }
         }
        jtfName.setText("");
        jtfSurname.setText("");
        jtfID.setText("");
        jtfNumberOfEmployee.setText("");
        jtfWorkedHours.setText("");
                
       
        //addCarDriverToVector(carDriver);
    }//GEN-LAST:event_jButtonInsertActionPerformed

